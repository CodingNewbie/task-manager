class Task {
    constructor (name, assignee, dueDate, section, priority, status, budget, description) {
        this.name = name;
        this.assignee = assignee;
        this.dueDate = dueDate;
        this.section = section;
        this.priority = priority;
        this.status = status;
        this.budget = budget;
        this.description = description;
    }
}